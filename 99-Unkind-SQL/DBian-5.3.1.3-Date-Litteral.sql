ALTER SESSION SET NLS_DATE_FORMAT = "YYYY-MM-DD HH24:MI:SS";

SELECT 
    SYSDATE
FROM DUAL;

ALTER SESSION SET NLS_TIMESTAMP_FORMAT = "YYYY-MM-DD HH24:MI:SS.FF";

SELECT 
    SYSDATE
FROM DUAL;

ALTER SESSION SET NLS_TIMESTAMP_TZ_FORMAT = "YYYY-MM-DD HH24:MI:SS.FF TZH:TZM";

SELECT 
    SYSDATE
FROM DUAL;
-- NLS 파라미터 설정이 바뀌면 무엇이 바뀌는가?
-- ㄴNLS 포맷이 변하는거 같은데?
-- ALTER에 대해 더 배우고 돌아오자

SELECT 
    DATE '2050-01-01' AS c1,
    TO_DATE(
        '2050-01-01 23:59:29', 'YYYY-MM-DD HH24:MI:SS'
    ) AS c2
FROM DUAL;
-- TO_DATE를 이용한 리터럴 날짜 출력

SELECT TIMESTAMP '2050-01-01 23:59:59.999999999999' AS c1 FROM DUAL;
-- TIMESTAMP 리터럴은 소수점 이하 초 지정 가능

SELECT TIMESTAMP '2050-01-01 23:59:59.99999999 +09:00' AS c1 FROM DUAL; 
-- 시간대 변위 값 포함 가능

